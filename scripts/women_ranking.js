function ranking(data){
    let append_box = document.getElementById('append_box')
    append_box.innerHTML = "";
    console.log('hello')
    data.forEach(function(el){
        let div = document.createElement('div')
        let position = document.createElement('p')
        position.innerText = el.rank
        position.setAttribute('class','rank')
        let img_div = document.createElement('div')
        let img = document.createElement('img')
        img.src = el.image
        img_div.append(img)
        img_div.setAttribute('class','img_div')

        let div1 = document.createElement('div')
        let name = document.createElement('h4')
        name.innerText = el.name
        div1.setAttribute('class','name')
        div1.append(name)

        let div2 = document.createElement('div')
        let country = document.createElement('p')
        country.innerText = el.country
        div2.setAttribute('class','country')
        div2.append(country)

        let ratting = document.createElement('p')
        ratting.innerText = el.ratting
        ratting.setAttribute('class','ratting')

        div.append(position,img_div,div1,div2,ratting)
        append_box.append(div)
    });
}

